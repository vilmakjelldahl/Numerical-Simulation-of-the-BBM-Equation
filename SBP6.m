            +1/60*diag(ones(m-3,1),3)-3/20*diag(ones(m-2,1),2) + 3/4*diag(ones(m-1,1),1));
        
        D1(1:6,1:9)=[-21600/13649 104009/54596 30443/81894 -33311/27298 16863/27298 -15025/163788 0 0 0; 
