function [D1, HI] = SBP6(m,h)
% Create diagonal norm FD-SBP operators for the first derivative
% Sixth order accurate in the interior and third order accurate at the
% boundaries
% 12 boundary points

        H=diag(ones(m,1),0);
        H(1:6,1:6)=diag([13649/43200 12013/8640 2711/4320 5359/4320 7877/8640 43801/43200]);
